type Tile struct {
  id int
  t string
  r string
  b string
  l string
}

func Reverse(s string) string {
  r := ""
  for _, l := range s {
    r = string(l) + r
  }
  return r
}

func NewTile(spec string) Tile {
  tile := Tile {}

  nameMatcher := regexp.MustCompile("Tile ([0-9]+):")
  nameMatch := nameMatcher.FindStringSubmatch(spec)

  v, _ := strconv.Atoi(nameMatch[1])
  tile.id = v

  lastString := ""

  for y , line := range strings.Split(spec, "\n") {
    if y == 0 {
      continue
    } else if y == 1 {
      tile.t = line
    }

    lastString = line

    for x, val := range line {
      if x == 0 {
        tile.l += string(val)
      } else if x == len(line) - 1 {
        tile.r += string(val)
      }
    }
  }

  tile.b = lastString

  return tile
}

func P1 (tiles []Tile) int {
  ret := 1

  borderCounts := make(map[string]int)

  for _, tile := range tiles {
    borderCounts[tile.t]++
    borderCounts[tile.r]++
    borderCounts[tile.b]++
    borderCounts[tile.l]++

    borderCounts[Reverse(tile.t)]++
    borderCounts[Reverse(tile.r)]++
    borderCounts[Reverse(tile.b)]++
    borderCounts[Reverse(tile.l)]++
  }

  for _, tile := range tiles {
    borders := borderCounts[tile.t] +
               borderCounts[tile.r] +
               borderCounts[tile.b] +
               borderCounts[tile.l] +
               borderCounts[Reverse(tile.t)] +
               borderCounts[Reverse(tile.r)] +
               borderCounts[Reverse(tile.b)] +
               borderCounts[Reverse(tile.l)]
    if borders == 12 {
      ret *= tile.id
    }

    fmt.Printf("%d has %d borders\n", tile.id, borders)
  }

  return ret
}

